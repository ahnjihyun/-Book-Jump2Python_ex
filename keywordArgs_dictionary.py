
## ==============================================================
## 키워드 파라미터 : 매개변수 앞에 별 두개(**)를 붙인다
## key=value 형태의 딕셔너리가 생성된다
## ==============================================================

# 예시 
def keyword_args(**kwargs):
    print(kwargs)

print(keyword_args(name='kildong', age=20))
# { 'name' : 'kildong', 'age' : 20 }

